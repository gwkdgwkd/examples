//     case a+10: printf("..."); break;  // 错误，不能包含变量
// 2) default不是必须的。当没有default时，如果所有case都匹配失败，那么就什么都不执行。

// do-while循环与while循环的不同在于：它会先执行“语句块”，然后再判断表达式是否为真，如果为真则继续循环；如果为假，则终止循环。因此，do-while循环至少要执行一次“语句块”。
