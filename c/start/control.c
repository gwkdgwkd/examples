// continue语句的作用是跳过循环体中剩余的语句而强制进入下一次循环。continue语句只用在 while、for循环中，常与if条件语句一起使用，判断条件是否成立。
// break与continue的对比：break用来结束所有循环，循环语句不再有执行的机会；continue用来结束本次循环，直接跳到下一次循环，如果循环条件成立，还会继续循环。

