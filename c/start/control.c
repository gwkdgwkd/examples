// switch
// 需要重点强调的是，当和某个整型数值匹配成功后，会执行该分支以及后面所有分支的语句。
// break是C语言中的一个关键字，专门用于跳出switch语句。所谓“跳出”，是指一旦遇到break，就不再执行switch中的任何语句，包括当前分支中的语句和其他分支中的语句；
// 也就是说，整个switch执行结束了，接着会执行整个switch后面的代码。
// default是最后一个分支，匹配后不会再执行其他分支，所以也可以不添加break;语句。
// 需要说明的两点是：
// 1) case后面必须是一个整数，或者是结果为整数的表达式，但不能包含任何变量。请看下面的例子：
//     case 10: printf("..."); break;  // 正确
//     case 8+9: printf("..."); break;  // 正确
//     case 'A': printf("..."); break;  // 正确，字符和整数可以相互转换
